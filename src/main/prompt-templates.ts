/**
 * Returns the 6 built-in prompt templates.
 */
export function getDefaultTemplates(): PromptTemplate[] {
  return BUILT_IN_TEMPLATES
}

/**
 * Seeds the built-in templates into the database.
 * Uses upsert so existing built-in templates get updated
 * but user-created templates are never touched.
 */
export function seedDefaultTemplates(): void {
  const repo = new PromptTemplateRepo()
  const templates = getDefaultTemplates()

  for (const template of templates) {
    try {
      repo.save(template)
    } catch (err) {
      logger.error('Failed to seed prompt template', { id: template.id, err })
    }
  }

  logger.info(`Seeded ${templates.length} built-in prompt templates`)
}
